
x = 9
y = 5
def math_operation(a, b, operation):
    if operation == "add":
        print(a+b)
    if operation == "sub":
        print(a - b)
    if operation == "multi":
        print(a * b)
    if operation == "divide":
        print(a / b)
op = "divide"
math_operation(x, y, op)


